    private void tampil_id_otomatis(){
        try{
            java.sql.Connection conn = (Connection)config.configDB();
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery("select id_bantu as a from tb_kamar order by id_bantu desc");
            
            if(rs.next()){
                String no_urut = rs.getString("a");
                int a = Integer.parseInt(no_urut);
                int panjang = no_urut.length();
                for (int i = 0; i < 2 - panjang; i++) {
                    a = a;
                }
                tf_nokamar.setText("KM00"+Integer.toString(a+1));
                tf_id_bantu.setText(Integer.toString(a+1));
            }else{
                tf_nokamar.setText("KM001");
                tf_id_bantu.setText("1");
            }
            
            rs.close();
        }catch (Exception e){
           JOptionPane.showMessageDialog(null, e);
        }
    }
    
