    private void btn_selengkapnyaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selengkapnyaActionPerformed
        // TODO add your handling code here:
        //selengkapnya
        
        int i = jTable1.getSelectedRow();
        TableModel tbl = jTable1.getModel();
             
        String no = tbl.getValueAt(i, 0).toString();
        String fasilitas = tbl.getValueAt(i, 1).toString();
        String lantai = tbl.getValueAt(i, 2).toString();
        String ukuran = tbl.getValueAt(i, 3).toString();
        String harga = tbl.getValueAt(i, 4).toString();
       
        if (no==null) {
            lbl_no_kamar.setText("");
            judul_kamar.setText("");
        } else{
            lbl_no_kamar.setText(no);
            judul_kamar.setText(no);
        }
        if (fasilitas==null) {
            lbl_fasilitas.setText("");
        } else{
            lbl_fasilitas.setText(fasilitas);
        }
        if (lantai==null) {
            lbl_lantai.setText(lantai);
        } else{
            lbl_lantai.setText(lantai);
        }
        if (ukuran==null) {
            lbl_ukuran.setText("");
        } else{
            lbl_ukuran.setText(ukuran);
    
        }
        if (harga==null) {
            lbl_harga.setText("");
        } else{
            lbl_harga.setText(harga);
        }
        try{
            databaru = false;
            java.sql.Connection conn = (Connection)config.configDB();
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("SELECT * FROM tb_kamar join tb_penghuni on tb_kamar.noKamar = tb_penghuni.noKamar WHERE tb_kamar.noKamar='" +no+"'");
            
            if (result.next()){
                    lbl_nama_penghuni.setText(result.getString("nama_penghuni"));
            }else {
               lbl_nama_penghuni.setText("-");
            }

        } catch (SQLException ex) {
            Logger.getLogger(form_kamar.class.getName()).log(Level.SEVERE, null, ex);
        }
  
        try{          
            java.sql.Connection conn = (Connection)config.configDB();
            Statement stm1 = conn.createStatement();
            Statement stm2 = conn.createStatement();
            Statement stm3 = conn.createStatement();
            ResultSet result1 = stm1.executeQuery("SELECT img FROM tb_kamar_img where noKamar = '"+no+"' AND index_img = '1'");
            ResultSet result2 = stm2.executeQuery("SELECT img FROM tb_kamar_img where noKamar = '"+no+"' AND index_img = '2'");
            ResultSet result3 = stm3.executeQuery("SELECT img FROM tb_kamar_img where noKamar = '"+no+"' AND index_img = '3'");
      
            if (result1.next()){
                String query = result1.getString("img");
                ImageIcon icon = new ImageIcon(query);
                Image image = icon.getImage().getScaledInstance(img1.getWidth(), img1.getHeight(), Image.SCALE_DEFAULT);
                ImageIcon ic = new ImageIcon(image);
                img1.setIcon(ic);
            }else {
               JOptionPane.showMessageDialog(null, "Gagal menampilkan gambar 1");
            }
            
            if (result2.next()){
                String query = result2.getString("img");
                ImageIcon icon = new ImageIcon(query);
                Image image = icon.getImage().getScaledInstance(img2.getWidth(), img2.getHeight(), Image.SCALE_DEFAULT);
                ImageIcon ic = new ImageIcon(image);
                img2.setIcon(ic);
            }else {
               JOptionPane.showMessageDialog(null, "Gagal menampilkan gambar 2");
            }
            
            if (result3.next()){
                String query = result3.getString("img");
                ImageIcon icon = new ImageIcon(query);
                Image image = icon.getImage().getScaledInstance(img3.getWidth(), img3.getHeight(), Image.SCALE_DEFAULT);
                ImageIcon ic = new ImageIcon(image);
                img3.setIcon(ic);
            }else {
               JOptionPane.showMessageDialog(null, "Gagal menampilkan gambar 3");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan (catch)"+ex);
        }
        titik_img2.setVisible(false);
        titik_img3.setVisible(false);
        jPanel3.setVisible(true);
        
    }//GEN-LAST:event_btn_selengkapnyaActionPerformed

