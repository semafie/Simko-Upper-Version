    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:         
        tf_nokamar.getText();        
        if (databaru = true) {
            try {
                String sql = "INSERT INTO tb_kamar (noKamar, fasilitas, lantai, "
                        + "ukuran, harga, status, id_bantu) VALUES ('%s', '%s', "
                        + "'%s', '%s', '%s', 'Kosong', '%s')";           
            sql = String.format(
                    sql,
                    tf_nokamar.getText(),
                    tf_fasilitas.getText(),
                    cb_lantai.getSelectedItem(),
                    tf_ukuran.getText(),
                    reFormat(tf_harga.getText()),
                    tf_id_bantu.getText()
            );           
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();             
            if(tf_link_gambar1.getText().equals("")){
                 JOptionPane.showMessageDialog(null, "Kosong,blm memilih gambar");                
            } else{
                try {
                    String no = tf_nokamar.getText();
                    String query1 = tf_link_gambar1.getText();
                    String query2 = tf_link_gambar2.getText();
                    String query3 = tf_link_gambar3.getText();
                    String sql1 = "insert into tb_kamar_img (noKamar, img, index_img) values "
                            + "('"+no+"','"+query1+"','1')";
                    String sql2 = "insert into tb_kamar_img (noKamar, img, index_img) values "
                            + "('"+no+"','"+query2+"','2')";
                    String sql3 = "insert into tb_kamar_img (noKamar, img, index_img) values "
                            + "('"+no+"','"+query3+"','3')";
                    
                    java.sql.PreparedStatement pst1 = conn.prepareStatement(sql1);
                    java.sql.PreparedStatement pst2 = conn.prepareStatement(sql2);
                    java.sql.PreparedStatement pst3 = conn.prepareStatement(sql3);
                    pst1.execute();
                    pst2.execute();
                    pst3.execute();
                    pop_up_berhasilSimpan.setVisible(true);
                 } catch (SQLException e){
                    JOptionPane.showMessageDialog(null, "Gambar gagal disimpan" + e);
                 } 
            }           
            jPanel1.setVisible(false);
            ImageIcon ic = new ImageIcon("<none>");
            up_img1.setIcon(ic);
            up_img2.setIcon(ic);
            up_img3.setIcon(ic);
            } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal disimpan"+ex);
            }
            resetLink();           
        }
        getData();
        resetData();
      
    }//GEN-LAST:event_simpanMouseClicked

